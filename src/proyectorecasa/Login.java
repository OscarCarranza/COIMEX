    public int nextId(){
        
        DefaultTableModel model = (DefaultTableModel)tableProducts.getModel();
        
        int row = 1;
        int id1, id2 = 1;
        int nextId = 1;
        while(row < model.getRowCount()-1){
            id1 = Integer.parseInt(model.getValueAt(row, 0).toString());
            id2 = Integer.parseInt(model.getValueAt(row+1, 0).toString());
            
            if((id2 - id1) != 1){
                nextId = id1+1;
                break;
            }
            row++;
        }
        if(nextId == 1){
            nextId = row+2;
        }
        return nextId;        
    }
